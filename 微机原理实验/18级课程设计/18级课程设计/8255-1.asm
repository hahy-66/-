	.MODEL	TINY	
COM_ADD	EQU	0F003H	
PA_ADD	EQU	0F000H	
PB_ADD	EQU	0F001H	
PC_ADD	EQU	0F002H	
	.STACK		1024	
	.DATA		
	.CODE		
START:	MOV	AX,@DATA	
	MOV	DS,AX	
		
	MOV	DX,COM_ADD	
	MOV	AL,80H	;PA、PB、PC为基本输出模式
	OUT	DX,AL	
	MOV	DX,PA_ADD	;灯全熄灭
	MOV	AL,0FFH	
	OUT	DX,AL
	MOV	AL,0FEH		
START1:	OUT	DX,AL	
	ROL     AL,1		
	CALL	DL500ms	
	JMP	START1	
	
DL500ms	PROC		NEAR	
	PUSH	CX	
	MOV	CX,60000	
DL500ms1:LOOP	DL500ms1	
	POP	CX	
	RET		
DL500ms	ENDP
		
	
	END		START	


